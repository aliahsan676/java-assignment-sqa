package assignment3;

public class ifElseIf {
    public static void main(String[] args) {
        int salary = 6500;
        if (salary >= 8000){
            System.out.println("High Salary");
        } else if (salary >= 7000) {
            System.out.println("Higher - Mid Level Salary");

        } else if (salary >= 6000) {
            System.out.println("Mid Salary");


        }else{
            System.out.println("Low Salary");
        }


    }
}
